export class Story {
    constructor(characters, comics, creators, id, originalIssue, series, title, type) {
        this.characters = characters;
        this.comics = comics;
        this.creators = creators;
        this.id = id;
        this.originalIssue = originalIssue;
        this.series = series;
        this.title = title;
        this.type = type;
    };
}