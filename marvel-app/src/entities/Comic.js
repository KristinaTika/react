export class Comic {
    constructor(characters, creators, description, format, isbn, issueNumber, pages, price, series, stories, title, image, id) {
        this.characters = characters;
        this.creators = creators;
        this.description = description;
        this.format = format;
        this.isbn = isbn;
        this.issueNumber = issueNumber;
        this.pages = pages;
        this.price = price; 
        this.series = series;
        this.stories = stories;
        this.title = title;
        this.image = image;
        this.id = id;
    }
}