export class Serie {
    constructor(characters, creators, description, endYear, startYear, id, image, title, type) {
        this.characters = characters;
        this.creators = creators;
        this.description = description;
        this.endYear = endYear;
        this.startYear = startYear;
        this.id = id;
        this.image = image;
        this.title = title;
        this.type = type;
    }
}